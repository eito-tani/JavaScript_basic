// 朝のあいさつを出力する関数を定義する
const sayGoodMorning = () => {
 console.log('おはようございます！');
 console.log('昨日はよく眠れましたか？');
 console.log('今日も一日頑張りましょう！');
}

// 夜のあいさつを出力する関数を定義する
const sayGoodEvning = () => {
 console.log('こんばんは！');
 console.log('今日も1日お疲れ様でした。');
}

// 朝のあいさつを出力する関数を呼び出す
sayGoodMorning();

// 夜のあいさつを出力する関数を呼び出す
sayGoodEvning();